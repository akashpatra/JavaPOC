package diff;

import java.util.HashMap;
import java.util.Hashtable;

/**
 * HashTable Vs HashMap
 *
 * @author Akash Patra
 */
public class HashTableVsHashMap {

    public static void main(String[] args) {
        // HashTable
        Hashtable hashtable = new Hashtable();
        hashtable.put("One", 1);

        // HashMap
        HashMap hashMap = new HashMap();
        hashMap.put("Two", 2);
    }
}
